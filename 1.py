a = 5
b = 10
print(f"Initial values: a = {a}, b = {b}")
def exchange_values(x, y):
    """Function to exchange values of two variables."""
    return y, x
# Exchanging values of a and b
a, b = exchange_values(a, b)
# The values of a and b are now exchanged.
# After exchanging, the values of a and b will be swapped.
print(f"Exchanged values: a = {a}, b = {b}")
# The values of a and b have been exchanged successfully.
